import { useState, useMemo } from 'react';
import { useTranslation } from 'react-i18next';
import {
  GraduationCap,
  Search,
  Clock,
  MessageSquare,
  Users,
  Plus,
} from 'lucide-react';
import {
  getAllSchoolProgressKeys,
  loadSchoolProgress,
  extendProAccess,
  getCompletedStampCount,
} from '../../utils/schoolStorage';
import { TOTAL_PERIODS } from '../../types/school';

interface StudentSchoolData {
  userId: string;
  stamps: number;
  totalPeriods: number;
  isGraduated: boolean;
  graduatedAt?: string;
  review?: string;
  proExpiresAt?: string;
  hasAptitude: boolean;
  hasSimulation: boolean;
}

export default function SchoolManagement() {
  const { t } = useTranslation('common');
  const [searchQuery, setSearchQuery] = useState('');
  const [activeSection, setActiveSection] = useState<'reviews' | 'students'>('students');
  const [extensionDays, setExtensionDays] = useState(30);

  // Ï†ÑÏ≤¥ ÌïôÍµê Îç∞Ïù¥ÌÑ∞ Î°úÎìú
  const allData: StudentSchoolData[] = useMemo(() => {
    const keys = getAllSchoolProgressKeys();
    return keys.map((userId) => {
      const progress = loadSchoolProgress(userId);
      const stamps = progress.stamps.filter((s) => s.completed).length;
      return {
        userId,
        stamps,
        totalPeriods: TOTAL_PERIODS,
        isGraduated: progress.graduation.isGraduated,
        graduatedAt: progress.graduation.graduatedAt,
        review: progress.graduation.review,
        proExpiresAt: progress.graduation.proExpiresAt,
        hasAptitude: !!progress.aptitudeResult,
        hasSimulation: !!progress.simulationResult,
      };
    });
  }, []);

  const filteredData = allData.filter((d) =>
    d.userId.toLowerCase().includes(searchQuery.toLowerCase())
  );

  const graduatedStudents = filteredData.filter((d) => d.isGraduated);
  const reviewStudents = allData.filter((d) => d.review);

  const handleExtendPro = (userId: string) => {
    extendProAccess(userId, extensionDays);
    alert(t('school.admin.extended', { days: extensionDays }));
  };

  return (
    <div className="space-y-6">
      {/* ÌÜµÍ≥Ñ Ïπ¥Îìú */}
      <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
        <div className="bg-white rounded-xl border border-gray-100 p-5">
          <div className="flex items-center gap-3">
            <div className="w-10 h-10 bg-purple-50 rounded-lg flex items-center justify-center">
              <Users className="w-5 h-5 text-purple-600" />
            </div>
            <div>
              <p className="text-2xl font-bold text-gray-800">{allData.length}</p>
              <p className="text-xs text-gray-500">{t('school.admin.totalEnrolled')}</p>
            </div>
          </div>
        </div>
        <div className="bg-white rounded-xl border border-gray-100 p-5">
          <div className="flex items-center gap-3">
            <div className="w-10 h-10 bg-green-50 rounded-lg flex items-center justify-center">
              <GraduationCap className="w-5 h-5 text-green-600" />
            </div>
            <div>
              <p className="text-2xl font-bold text-gray-800">{graduatedStudents.length}</p>
              <p className="text-xs text-gray-500">{t('school.admin.totalGraduated')}</p>
            </div>
          </div>
        </div>
        <div className="bg-white rounded-xl border border-gray-100 p-5">
          <div className="flex items-center gap-3">
            <div className="w-10 h-10 bg-blue-50 rounded-lg flex items-center justify-center">
              <MessageSquare className="w-5 h-5 text-blue-600" />
            </div>
            <div>
              <p className="text-2xl font-bold text-gray-800">{reviewStudents.length}</p>
              <p className="text-xs text-gray-500">{t('school.admin.totalReviews')}</p>
            </div>
          </div>
        </div>
      </div>

      {/* ÏÑπÏÖò ÌÉ≠ */}
      <div className="flex gap-1 bg-gray-100 p-1 rounded-xl">
        <button
          onClick={() => setActiveSection('students')}
          className={`flex items-center gap-2 px-5 py-2.5 rounded-lg text-sm font-medium transition-all flex-1 justify-center ${
            activeSection === 'students'
              ? 'bg-white text-gray-800 shadow-sm'
              : 'text-gray-500 hover:text-gray-700'
          }`}
        >
          <Users className="w-4 h-4" />
          {t('school.admin.studentsSection')}
        </button>
        <button
          onClick={() => setActiveSection('reviews')}
          className={`flex items-center gap-2 px-5 py-2.5 rounded-lg text-sm font-medium transition-all flex-1 justify-center ${
            activeSection === 'reviews'
              ? 'bg-white text-gray-800 shadow-sm'
              : 'text-gray-500 hover:text-gray-700'
          }`}
        >
          <MessageSquare className="w-4 h-4" />
          {t('school.admin.reviewsSection')}
        </button>
      </div>

      {/* ÌïôÏÉù Î™©Î°ù */}
      {activeSection === 'students' && (
        <div className="bg-white rounded-xl border border-gray-100">
          <div className="p-5 border-b border-gray-100">
            <div className="flex items-center justify-between mb-3">
              <h3 className="font-semibold text-gray-800">{t('school.admin.enrolledStudents')}</h3>
              <div className="flex items-center gap-2">
                <input
                  type="number"
                  value={extensionDays}
                  onChange={(e) => setExtensionDays(Number(e.target.value))}
                  className="w-20 px-2 py-1.5 border border-gray-200 rounded-lg text-sm"
                  min={1}
                  max={365}
                />
                <span className="text-xs text-gray-500">{t('school.admin.daysLabel')}</span>
              </div>
            </div>
            <div className="relative">
              <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-gray-400" />
              <input
                type="text"
                placeholder={t('school.admin.searchPlaceholder')}
                value={searchQuery}
                onChange={(e) => setSearchQuery(e.target.value)}
                className="w-full pl-9 pr-4 py-2 border border-gray-200 rounded-lg text-sm focus:ring-2 focus:ring-purple-500 focus:border-transparent"
              />
            </div>
          </div>

          <div className="divide-y divide-gray-100">
            {filteredData.length === 0 ? (
              <div className="text-center py-12 text-gray-400">
                {t('school.admin.noStudents')}
              </div>
            ) : (
              filteredData.map((student) => (
                <div key={student.userId} className="px-5 py-4 hover:bg-gray-50">
                  <div className="flex items-center justify-between">
                    <div>
                      <p className="font-medium text-gray-800 font-mono text-sm">{student.userId}</p>
                      <div className="flex items-center gap-3 mt-1">
                        <span className="text-xs text-gray-500">
                          üèÜ {student.stamps}/{student.totalPeriods}
                        </span>
                        {student.isGraduated && (
                          <span className="text-xs text-green-600 font-medium">üéì {t('school.admin.graduated')}</span>
                        )}
                        {student.hasAptitude && (
                          <span className="text-xs text-blue-600">üìã</span>
                        )}
                        {student.proExpiresAt && (
                          <span className="text-xs text-gray-400 flex items-center gap-1">
                            <Clock className="w-3 h-3" />
                            ~{new Date(student.proExpiresAt).toLocaleDateString('ko-KR')}
                          </span>
                        )}
                      </div>
                    </div>
                    {student.isGraduated && (
                      <button
                        onClick={() => handleExtendPro(student.userId)}
                        className="flex items-center gap-1 px-3 py-1.5 text-xs font-medium bg-purple-50 text-purple-600 rounded-lg hover:bg-purple-100 transition-colors"
                      >
                        <Plus className="w-3 h-3" />
                        {t('school.admin.extend')}
                      </button>
                    )}
                  </div>
                </div>
              ))
            )}
          </div>
        </div>
      )}

      {/* Ï°∏ÏóÖ ÌõÑÍ∏∞ */}
      {activeSection === 'reviews' && (
        <div className="space-y-4">
          {reviewStudents.length === 0 ? (
            <div className="bg-white rounded-xl border border-gray-100 p-12 text-center text-gray-400">
              {t('school.admin.noReviews')}
            </div>
          ) : (
            reviewStudents.map((student) => (
              <div key={student.userId} className="bg-white rounded-xl border border-gray-100 p-5">
                <div className="flex items-center justify-between mb-3">
                  <span className="font-mono text-sm text-gray-600">{student.userId}</span>
                  <span className="text-xs text-gray-400">
                    {student.graduatedAt && new Date(student.graduatedAt).toLocaleDateString('ko-KR')}
                  </span>
                </div>
                <p className="text-gray-800 text-sm leading-relaxed bg-gray-50 rounded-lg p-4">
                  "{student.review}"
                </p>
              </div>
            ))
          )}
        </div>
      )}
    </div>
  );
}
